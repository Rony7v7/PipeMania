package model;

/**
 * The PipeLineType enumeration represents the different types of pipes available in the
 * Pipe Mania Game. Each type of pipe has a specific purpose in the game
 */
public enum PipeLineType {
    HORIZONTAL, // Horizontal pipe
    VERTICAL, // Vertical pipe
    CIRCULAR, // Circular pipe
    VOID, // Empty pipe
    SOURCE, // Source pipe
    DRAINAGE // Drain pipe
}
